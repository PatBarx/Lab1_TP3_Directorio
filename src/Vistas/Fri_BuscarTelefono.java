    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        List<Telefono> numList = Frm_TP3_NewJframeMain.guiaTel.buscarTelefono(jtf_apellidoU.getText());
        if (numList.size() > 0) {
            for (Telefono telefono : numList) {
                jta_fonos.setText(jta_fonos.getText()+"\n" + String.valueOf(telefono.getNumero()));
            }
        } else {
            JOptionPane.showMessageDialog(null, "Apellido no arroja resultados");
        }
    }//GEN-LAST:event_btn_buscarActionPerformed

